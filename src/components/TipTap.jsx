const TipTap = ({ setDesc }) => {
  const editor = useEditor({
    extensions: [StarterKit, Underline],

    content: `<h2>
                  Seja bem vindo ao meu projeto de editor com formatação Rich-text
                  <br> <br>
                  <i> Welcome to my editor project with rich-text formatting <i/>
              </h2>
              <p>
                  <br><br> 
                  <i>Conhecimentos(Skills)<i/><br><br>
                  <b>
                  Os conhecimentos necessários para este projeto foram React, JS e TipTap, para mais informações sobre o projeto leia o documento readme
                  <br><br><br>
                  The knowledge required for this project was React, JS and TipTap, for more information about the project read the readme document</b>

              </p>`,
                
    onUpdate: ({ editor }) => {
      const html = editor.getHTML();
      setDesc(html);
    },
  });

  return (
    <div className="text-editor">
      <MenuBar editor={editor} />
      <EditorContent editor={editor} />
    </div>
  );
};

export default TipTap;
